    <br> <h2  align="left"  class="service-heading titull" style="font-size:22px;">Exploración biosocial del enfoque de NES Health sobre la salud y el bienestar de las personas con diversas afecciones físicas<br><p style="color:#757575;"> por la profesora Anna van Wersch</p></h2>
    <p align="justify" class="font-weight-normal  j">
    Este estudio fenomenológico se llevó a cabo en la Universidad de Teesside en el Reino Unido, bajo la dirección de la profesora Anna van Wersch, una reconocida experta en psicología de la salud.
    </p>
    <p align="justify" class="font-weight-normal  j">
    El estudio realizado por el profesor van Wersch fue un 'Exploración biopsicosocial del enfoque del sistema NES HEALTH sobre la salud y el bienestar de las personas con diversas afecciones físicas'.
    </p>
    <p align="justify" class="font-weight-normal  j">
    El objetivo del estudio fue investigar el efecto del protocolo de tratamiento NES HEALTH en un grupo de cinco personas con síntomas sin explicación médica (MUS) que no cumplían los criterios para obtener una etiología orgánica clara o exacta y, por lo tanto, representaban un caso problemático para Consulta médica. Los individuos incluidos en este estudio fueron diagnosticados con fibromialgia, encefalomielitis miálgica (ME) y síndrome del intestino irritable (SII), todos reconocidos por simbolizar formas crónicas e incapacitantes de la enfermedad. La investigación anecdótica previa sobre el uso de NES HEALTH para este grupo de sujetos es convincente; Sin embargo, carece de exploración meticulosa y de escrutinio científico. Por esta razón,
    </p>
    <p align="justify" class="font-weight-normal  j">
    Los hallazgos de investigación establecieron medidas pre y post fisiológicas de; Grabaciones de cortisol, sIgA, presión arterial y frecuencia del pulso que dan resultados objetivos positivos de la intervención. Las medidas subjetivas también demostraron un efecto positivo de la terapia NES HEALTH en todos menos uno de los casos, en este caso parecía haber una mejora fisiológica y psicológica, pero esto era contrario a la experiencia subjetiva del sujeto.
    </p>
    <p align="justify" class="font-weight-normal  j">
    Esta exploración multifacética bien realizada de NES HEALTH proporciona algunos datos científicos sólidos sobre la eficacia del enfoque de NES HEALTH para el apoyo a las personas que padecen estas afecciones convencionalmente difíciles de tratar, a un mejor estado de bienestar.
